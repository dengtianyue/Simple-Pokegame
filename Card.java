package Pokegame;
/**
 * 定义类 Card及其set，get方法
 * @author tyt
 *
 */
public class Card {
	String cardShape;
	String cardId;
	public String getCardShape() {
		return cardShape;
	}
	public void setCardShape(String cardShape) {
		this.cardShape = cardShape;
	}
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	

}
